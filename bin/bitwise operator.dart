void main() {
  bool a = true;
  bool b = false;
  //bitwies AND operator
  var c = a & b;
  print(c);
  //bitwies or operator
  var d = a | b;
  print(d);
  //bitwies xor operator
  var e = a ^ b;
  print(e);
  //using integer to declare bitwies operator
  int x = 10;
  int y = 30;
  int z = 51;
  //bitwise not operator
  var f = ~z;
  print(f);
  //bitwies left shift
  var g = x << y;
  print(g);
  //bitwies right shift
  var h = x >> y;
  print(h);
  //bitwies and using integer
  var j = x & y;
  print(j);
  //bitwies or
  var i = x | y;
  print(i);
  //bitwies xor
  var k = x ^ y;
  print(k);
}
